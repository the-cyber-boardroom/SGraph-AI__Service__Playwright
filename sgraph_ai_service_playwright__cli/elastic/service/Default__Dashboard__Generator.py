# SP CLI — Default__Dashboard__Generator
# Builds the saved-objects ndjson for the "Synthetic Logs Overview" dashboard
# that `sp el seed` creates by default. Programmatic generation rather than a
# checked-in fixture file because every panel needs the data view id baked
# into its `references` array — the id is unknown until ensure_data_view ran.
#
# Output panels (Lens, formBased datasource):
#   1. Log levels                  — donut, terms on level.keyword
#   2. Log volume over time        — stacked bar, date_histogram on timestamp,
#                                    split by level.keyword
#   3. Top services                — horizontal bar, terms on service.keyword
#   4. Average duration over time  — line, date_histogram + avg(duration_ms)
#
# IDs are deterministic so re-running seed with overwrite=true is idempotent.
# ═══════════════════════════════════════════════════════════════════════════════

