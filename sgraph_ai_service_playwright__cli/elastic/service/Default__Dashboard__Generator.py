def panel(panel_index: str, ref_name: str, x: int, y: int, w: int, h: int) -> dict:
    return {'version'         : '8.13.4',
            'type'            : 'lens',
            'gridData'        : {'x': x, 'y': y, 'w': w, 'h': h, 'i': panel_index},
            'panelIndex'      : panel_index,
            'embeddableConfig': {'enhancements': {}},
            'panelRefName'    : ref_name}


class Default__Dashboard__Generator(Type_Safe):

    @type_safe
    def build_ndjson(self, index        : str ,                                     # ES index name (also the data view title)
                            data_view_id : str ,                                     # Kibana data view UUID — baked into every reference
                            time_field   : str = 'timestamp'
                       ) -> bytes:
        # Layout: 2x2 grid, each panel 24w x 12h on a 48-column canvas (Kibana default)
        panels = [panel('p1', 'panel_lens-log-levels'     ,  0,  0, 24, 12),
                  panel('p2', 'panel_lens-volume-by-level', 24,  0, 24, 12),
                  panel('p3', 'panel_lens-top-services'   ,  0, 12, 24, 12),
                  panel('p4', 'panel_lens-avg-duration'   , 24, 12, 24, 12)]
        panels_json_string = json.dumps(panels)

        objects: List[dict] = [
            lens_log_levels     (data_view_id),
            lens_volume_by_level(data_view_id, time_field),
            lens_top_services   (data_view_id),
            lens_avg_duration   (data_view_id, time_field),
            dashboard           (panels_json_string),
        ]
        # ndjson: one JSON object per line, no trailing comma, no enclosing array
        return ('\n'.join(json.dumps(o) for o in objects) + '\n').encode('utf-8')

    def dashboard_id(self) -> str:                                                  # So callers can render it without importing the module-level constant
        return DASHBOARD_ID

    def dashboard_title(self) -> str:
        return DASHBOARD_TITLE
