# ═══════════════════════════════════════════════════════════════════════════════
# SP CLI — CF__Inventory__Dashboard__Builder
# Builds the saved-objects ndjson for the "CloudFront Logs - Inventory Overview"
# dashboard.  Mirrors Default__Dashboard__Generator for the synthetic dataset
# — same legacy `visualization` saved-object type (Vis Editor), same
# deterministic-ids pattern, same Kibana-JSON-as-string conventions.
#
# Lens explicitly avoided.  The synthetic-data slice's debrief (sp-elastic-
# kibana 02-what.md, "Notable bugs") flagged Lens migration footguns as the
# #1 reason hand-rolled saved-objects fail to import.  We stay on Vis Editor.
#
# Five panels, each sourced from the sg-cf-inventory-* data view (time
# field: delivery_at):
#   1. Object count over time          line, count() over date_histogram(delivery_at, hourly)
#   2. Total bytes over time           stacked vertical bar, sum(size_bytes) over hourly histogram, split by terms(delivery_day)
#   3. Object size distribution        vertical bar, count() bucketed on size_bytes (numeric histogram, 50KB interval)
#   4. Storage class breakdown         donut, count() with terms on storage_class.keyword
#   5. Activity by hour of day         horizontal bar, count() with terms on delivery_hour
#
# Layout: 2x2 grid + a full-width fifth row, on Kibana's 48-column canvas.
# ═══════════════════════════════════════════════════════════════════════════════

