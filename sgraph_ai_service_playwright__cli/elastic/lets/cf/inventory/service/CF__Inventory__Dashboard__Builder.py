# Reference name Kibana looks up when resolving the data view on a Vis Editor
# visualization. The legacy convention encodes the path inside the JSON-string
# searchSourceJSON.
DATA_VIEW_REF_NAME    = 'kibanaSavedObjectMeta.searchSourceJSON.index'


def search_source_json() -> str:                                                    # The kibanaSavedObjectMeta.searchSourceJSON value (JSON-encoded string)
    return json.dumps({'query'        : {'language': 'kuery', 'query': ''},
                       'filter'       : []                                  ,
                       'indexRefName' : DATA_VIEW_REF_NAME                  })


def vis_object(vis_id: str, title: str, vis_state: dict, data_view_id: str) -> dict:
    return {
        'id'        : vis_id,
        'type'      : 'visualization',
        'attributes': {
            'title'                 : title,
            'visState'              : json.dumps(vis_state),                         # visState lives as a JSON-encoded string — Kibana convention
            'uiStateJSON'           : '{}',
            'description'           : '',
            'version'               : 1,
            'kibanaSavedObjectMeta' : {'searchSourceJSON': search_source_json()},
        },
        'references': [
            {'name': DATA_VIEW_REF_NAME, 'type': 'index-pattern', 'id': data_view_id},
        ],
    }


