def panel(panel_index: str, x: int, y: int, w: int, h: int) -> dict:                # panelRefName = "panel_<panelIndex>" so the dashboard's references array can resolve the visualization id
    return {'version'         : '8.13.4',
            'type'            : 'visualization',
            'gridData'        : {'x': x, 'y': y, 'w': w, 'h': h, 'i': panel_index},
            'panelIndex'      : panel_index,
            'embeddableConfig': {'enhancements': {}},
            'panelRefName'    : f'panel_{panel_index}'}


class CF__Inventory__Dashboard__Builder(Type_Safe):

    @type_safe
    def build_ndjson(self, data_view_id : str ,                                     # Kibana data view UUID — baked into every reference
                            time_field   : str = 'delivery_at'
                       ) -> bytes:
        # Layout: 2x2 grid (panels 1-4) + full-width row (panel 5), on a 48-column canvas
        panels = [panel('p1',  0,  0, 24, 12),
                  panel('p2', 24,  0, 24, 12),
                  panel('p3',  0, 12, 24, 12),
                  panel('p4', 24, 12, 24, 12),
                  panel('p5',  0, 24, 48, 12)]
        panels_json_string = json.dumps(panels)

        objects: List[dict] = [
            vis_count_over_time         (data_view_id, time_field),
            vis_bytes_over_time         (data_view_id, time_field),
            vis_size_distribution       (data_view_id),
            vis_storage_class_breakdown (data_view_id),
            vis_activity_by_hour        (data_view_id),
            dashboard                   (panels_json_string),
        ]
        # ndjson: one JSON object per line, no trailing comma, no enclosing array
        return ('\n'.join(json.dumps(o) for o in objects) + '\n').encode('utf-8')

    def dashboard_id(self) -> str:                                                  # Convenience for callers that want the constant without importing the module
        return DASHBOARD_ID

    def dashboard_title(self) -> str:
        return DASHBOARD_TITLE
