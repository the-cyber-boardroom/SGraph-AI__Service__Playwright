def panel(panel_index: str, x: int, y: int, w: int, h: int) -> dict:
    return {'version'         : '8.13.4',
            'type'            : 'visualization',
            'gridData'        : {'x': x, 'y': y, 'w': w, 'h': h, 'i': panel_index},
            'panelIndex'      : panel_index,
            'embeddableConfig': {'enhancements': {}},
            'panelRefName'    : f'panel_{panel_index}'}


class CF__Events__Dashboard__Builder(Type_Safe):

    @type_safe
    def build_ndjson(self, data_view_id : str ,
                            time_field   : str = 'timestamp'
                       ) -> bytes:
        # Layout: 3x2 grid (3 rows, 2 panels each), 24w x 12h on a 48-column canvas
        panels = [panel('p1',  0,  0, 24, 12),
                  panel('p2', 24,  0, 24, 12),
                  panel('p3',  0, 12, 24, 12),
                  panel('p4', 24, 12, 24, 12),
                  panel('p5',  0, 24, 24, 12),
                  panel('p6', 24, 24, 24, 12)]
        panels_json_string = json.dumps(panels)

        objects: List[dict] = [
            vis_status_over_time   (data_view_id, time_field),
            vis_edge_result        (data_view_id),
            vis_top_uris           (data_view_id),
            vis_geographic         (data_view_id),
            vis_latency_percentiles(data_view_id, time_field),
            vis_bot_vs_human       (data_view_id, time_field),
            dashboard              (panels_json_string),
        ]
        return ('\n'.join(json.dumps(o) for o in objects) + '\n').encode('utf-8')

    def dashboard_id(self) -> str:
        return DASHBOARD_ID

    def dashboard_title(self) -> str:
        return DASHBOARD_TITLE
