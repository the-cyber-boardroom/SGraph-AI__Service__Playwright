# SP CLI — CF__Events__Dashboard__Builder
# Builds the saved-objects ndjson for the "CloudFront Logs - Events Overview"
# dashboard.  Mirrors slice 1's CF__Inventory__Dashboard__Builder pattern —
# legacy `visualization` saved-object type (Vis Editor), NOT Lens.  Same
# migration-safety reasoning as slice 1 (the synthetic-data debrief flagged
# Lens migration footguns).
#
# Six panels, each sourced from the sg-cf-events-* data view (time field:
# timestamp):
#   1. Status code distribution over time   stacked vertical bar
#   2. Edge result type breakdown            donut
#   3. Top URIs                              horizontal bar
#   4. Geographic distribution               donut
#   5. Time-taken percentiles over time      line (p50/p95/p99)
#   6. Bot vs human ratio over time          stacked vertical bar
#
# Layout: 3x2 grid (3 rows of 2 panels), each panel 24w x 12h on Kibana's
# 48-column canvas.
#
# Field naming notes (slice 1 lesson: terms aggs need .keyword on string
# fields; numeric fields use the raw name):
#   - sc_status_class.keyword       (text + .keyword)
#   - x_edge_result_type.keyword    (text + .keyword)
#   - cs_uri_stem.keyword           (text + .keyword)
#   - c_country.keyword             (text + .keyword)
#   - bot_category.keyword          (text + .keyword)
#   - time_taken_ms                 (long, numeric — direct)
# ═══════════════════════════════════════════════════════════════════════════════

