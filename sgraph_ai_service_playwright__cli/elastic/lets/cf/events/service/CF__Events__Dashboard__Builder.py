def search_source_json() -> str:
    return json.dumps({'query'        : {'language': 'kuery', 'query': ''},
                       'filter'       : []                                  ,
                       'indexRefName' : DATA_VIEW_REF_NAME                  })


def vis_object(vis_id: str, title: str, vis_state: dict, data_view_id: str) -> dict:
    return {
        'id'        : vis_id,
        'type'      : 'visualization',
        'attributes': {
            'title'                 : title,
            'visState'              : json.dumps(vis_state),
            'uiStateJSON'           : '{}',
            'description'           : '',
            'version'               : 1,
            'kibanaSavedObjectMeta' : {'searchSourceJSON': search_source_json()},
        },
        'references': [
            {'name': DATA_VIEW_REF_NAME, 'type': 'index-pattern', 'id': data_view_id},
        ],
    }


