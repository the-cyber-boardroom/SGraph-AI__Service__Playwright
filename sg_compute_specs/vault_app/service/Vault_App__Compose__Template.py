# ═══════════════════════════════════════════════════════════════════════════════
# SG/Compute Specs — vault-app: Vault_App__Compose__Template
# Renders the docker-compose.yml the EC2 host writes to /opt/vault-app/.
#
# Two shapes, selected by `with_playwright`:
#   just-vault     (default)  — host-plane + sg-send-vault            (2 containers)
#   with-playwright           — + sg-playwright + agent-mitmproxy     (4 containers)
#
# Only sg-send-vault publishes a host port (8080). Every other service stays on
# the internal vault-net bridge — reach them with an SSM port-forward.
#
# All ${...} values are resolved by the compose engine from /opt/vault-app/.env
# at `up` time — never interpolated here.
# ═══════════════════════════════════════════════════════════════════════════════

from osbot_utils.type_safe.Type_Safe import Type_Safe

SG_SEND_VAULT_IMAGE = 'diniscruz/sg-send-vault:latest'

_HEADER = '''\
# Generated by Vault_App__Compose__Template — do not edit by hand.
services:
'''

_HOST_PLANE = '''
  host-plane:
    image: {ecr_registry}/sgraph_ai_service_playwright_host:{image_tag}
    volumes:
      - {docker_socket}:/var/run/docker.sock
    environment:
      FAST_API__AUTH__API_KEY__NAME:  ${{FAST_API__AUTH__API_KEY__NAME:-X-API-Key}}
      FAST_API__AUTH__API_KEY__VALUE: ${{FAST_API__AUTH__API_KEY__VALUE}}{playwright_env_extras}
    networks:
      - vault-net
    restart: unless-stopped
'''

# Only added when --with-playwright: lets Routes__Web reach mitmweb across the
# compose network. The defaults in Routes__Web (127.0.0.1:8081) resolve to
# host-plane itself inside its own container — broken in compose. Pointing at
# the agent-mitmproxy service name is the fix.
_HOST_PLANE__PLAYWRIGHT_EXTRAS = '''
      AGENT_MITMPROXY__MITMWEB_HOST:   agent-mitmproxy
      AGENT_MITMPROXY__MITMWEB_PORT:   "8081"
      AGENT_MITMPROXY__ADMIN_API_PORT: "8000"'''

_SG_SEND_VAULT = '''
  sg-send-vault:
    image: {sg_send_vault_image}
    ports:
      - "8080:8080"
    volumes:
      - ${{VAULT_DATA_PATH:-/opt/vault-app/data}}:/data
    environment:
      SGRAPH_SEND__ACCESS_TOKEN:     ${{SGRAPH_SEND__ACCESS_TOKEN}}
      SEND__STORAGE_MODE:            ${{SEND__STORAGE_MODE:-disk}}
      SG_VAULT_APP__SEED_VAULT_KEYS: ${{SG_VAULT_APP__SEED_VAULT_KEYS:-}}
    networks:
      - vault-net
    restart: unless-stopped
'''

# TLS variant — sg-send-vault terminates its own HTTPS on :443 via the §8.2
# FAST_API__TLS__* contract, reading the cert the one-shot cert-init sidecar
# wrote to the shared volume. Gated behind cert-init via depends_on, so a failed
# issuance means the vault never starts (fail-loud, not silent plain-HTTP).
# With TLS on, the app binds :443 only — :8080 is no longer published.
_SG_SEND_VAULT_TLS = '''
  sg-send-vault:
    image: {sg_send_vault_image}
    ports:
      - "443:443"
    volumes:
      - ${{VAULT_DATA_PATH:-/opt/vault-app/data}}:/data
      - certs:/certs:ro
    environment:
      SGRAPH_SEND__ACCESS_TOKEN:     ${{SGRAPH_SEND__ACCESS_TOKEN}}
      SEND__STORAGE_MODE:            ${{SEND__STORAGE_MODE:-disk}}
      SG_VAULT_APP__SEED_VAULT_KEYS: ${{SG_VAULT_APP__SEED_VAULT_KEYS:-}}
      FAST_API__TLS__ENABLED:        "true"
      FAST_API__TLS__CERT_FILE:      /certs/cert.pem
      FAST_API__TLS__KEY_FILE:       /certs/key.pem
      FAST_API__TLS__PORT:           "443"
    networks:
      - vault-net
    depends_on:
      cert-init:
        condition: service_completed_successfully
    restart: unless-stopped
'''

_SG_PLAYWRIGHT = '''
  sg-playwright:
    image: {ecr_registry}/sgraph_ai_service_playwright:{image_tag}
    ports:
      - "11024:8000"             # external HTTP — same X-API-Key as the vault; see PLAYWRIGHT_EXTERNAL_PORT
    environment:
      FAST_API__AUTH__API_KEY__NAME:      ${{FAST_API__AUTH__API_KEY__NAME:-X-API-Key}}
      FAST_API__AUTH__API_KEY__VALUE:     ${{FAST_API__AUTH__API_KEY__VALUE}}
      SG_PLAYWRIGHT__DEPLOYMENT_TARGET:   container
      SG_PLAYWRIGHT__DEFAULT_PROXY_URL:   http://agent-mitmproxy:8080
      SG_PLAYWRIGHT__IGNORE_HTTPS_ERRORS: 'true'
    networks:
      - vault-net
    depends_on:
      - agent-mitmproxy
    restart: unless-stopped
'''

_AGENT_MITMPROXY = '''
  agent-mitmproxy:
    image: {ecr_registry}/agent_mitmproxy:{image_tag}
    environment:
      FAST_API__AUTH__API_KEY__NAME:  ${{FAST_API__AUTH__API_KEY__NAME:-X-API-Key}}
      FAST_API__AUTH__API_KEY__VALUE: ${{FAST_API__AUTH__API_KEY__VALUE}}
    networks:
      - vault-net
    restart: unless-stopped
'''

# One-shot cert sidecar — runs the host image (it already carries sg_compute),
# writes /certs/{cert,key}.pem, and exits 0. self-signed mode is offline;
# letsencrypt-ip mode runs an http-01 ACME challenge on :80 (hence the published
# port — harmless in self-signed mode, nothing listens after the sidecar exits).
# See the v0.2.6 TLS architecture doc §8.1.
_CERT_INIT = '''
  cert-init:
    image: {ecr_registry}/sgraph_ai_service_playwright_host:{image_tag}
    command: ["python", "-m", "sg_compute.platforms.tls.cert_init"]
    environment:
      SG__CERT_INIT__MODE:        ${{SG__CERT_INIT__MODE:-self-signed}}
      SG__CERT_INIT__COMMON_NAME: ${{SG__CERT_INIT__COMMON_NAME:-}}
      SG__CERT_INIT__ACME_PROD:   ${{SG__CERT_INIT__ACME_PROD:-false}}
      SG__CERT_INIT__ACME_EMAIL:  ${{SG__CERT_INIT__ACME_EMAIL:-}}
      FAST_API__TLS__CERT_FILE:   /certs/cert.pem
      FAST_API__TLS__KEY_FILE:    /certs/key.pem
    ports:
      - "80:80"
    volumes:
      - certs:/certs
    networks:
      - vault-net
    restart: "no"
'''

_NETWORKS = '''
networks:
  vault-net:
    driver: bridge
'''

_VOLUMES = '''
volumes:
  certs:
'''


class Vault_App__Compose__Template(Type_Safe):

    def render(self, ecr_registry        : str  ,
                     with_playwright     : bool = False                       ,
                     image_tag           : str  = 'latest'                    ,
                     sg_send_vault_image : str  = SG_SEND_VAULT_IMAGE          ,
                     docker_socket       : str  = '/var/run/docker.sock'      ,
                     with_tls_check      : bool = False                        ) -> str:
        vault_block            = (_SG_SEND_VAULT_TLS if with_tls_check else _SG_SEND_VAULT)
        playwright_env_extras  = _HOST_PLANE__PLAYWRIGHT_EXTRAS if with_playwright else ''
        parts = [
            _HEADER                                                                          ,
            _HOST_PLANE.format(ecr_registry          = ecr_registry          ,
                               image_tag             = image_tag             ,
                               docker_socket         = docker_socket         ,
                               playwright_env_extras = playwright_env_extras),
            vault_block.format(sg_send_vault_image=sg_send_vault_image)                       ,
        ]
        if with_playwright:
            parts.append(_SG_PLAYWRIGHT.format(ecr_registry=ecr_registry, image_tag=image_tag))
            parts.append(_AGENT_MITMPROXY.format(ecr_registry=ecr_registry, image_tag=image_tag))
        if with_tls_check:
            parts.append(_CERT_INIT.format(ecr_registry=ecr_registry, image_tag=image_tag))
        parts.append(_NETWORKS)
        if with_tls_check:
            parts.append(_VOLUMES)
        return ''.join(parts)
